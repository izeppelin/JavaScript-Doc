//Variables 
var // variables globales (usualmente es una mala práctica usarlo.
const //constante (permite crear variables)
let // variables de bloque 

/*
var: SI puede cambiar su valor y su scope es local.
const: NO cambiara su valor en ningún momento en el futuro. 
let: podra cambiar su valor, pero solo vivirá(Funcionara) en el bloque donde fue declarada.
*/